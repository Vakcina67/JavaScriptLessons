console.log(null != 0); // true
console.log(null == 0); // false
console.log(null > 0); // false
console.log(null < 0); // false
console.log(null >= 0); // true
console.log(null <= 0); // true
// null - пустое или несуществующее значение, 0 - это значение.
// при явном преобразовании (>=, <=) null принимает значение 0.